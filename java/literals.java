// literals are the values that is stored inside the variable
// by default float take double value so we use f or F to show it is float
// double always take double value by default
public class literals{
    public static void main(String[] args){
        byte age=34;
        char ch='a';
        float f1=5.6f;
        double d1=4.660d;
        short age1=34; //45678999
        long agedino=4555555555555l;
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(agedino);
        System.out.print(age1);
        String str="saloni";
         System.out.println(str);


    }
}